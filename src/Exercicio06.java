public class Exercicio06 {
    public void executar() {
        System.out.println("\n=== EXERCÍCIO 06: DIFERENÇA ABSOLUTA ===");

        int numero1 = 10;
        int numero2 = 5;

        int diferenca = numero1 - numero2;
        int valorAbsoluto = Math.abs(diferenca);

        System.out.println("Cálculo da diferença: " + diferenca);
        System.out.println("Valor absoluto: " + valorAbsoluto);
    }
}
